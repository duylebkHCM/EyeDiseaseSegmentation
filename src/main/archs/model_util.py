def set_trainable_attr(m, b):
    m.trainable = b
    for p in m.parameters():
        p.requires_grad = b


def apply_leaf(m, f):
    c = m if isinstance(m, (list, tuple)) else list(m.children())
    if isinstance(m, nn.Module):
        f(m)
    if len(c) > 0:
        for l in c:
            apply_leaf(l, f)


def set_trainable(l, b):
    apply_leaf(l, lambda m: set_trainable_attr(m, b))


def init_weights(model):
    for module in model.modules():
        if isinstance(module, nn.Conv2d) or isinstance(module, nn.Linear):
            nn.init.kaiming_normal_(module.weight)
            if module.bias is not None:
                module.bias.data.zero_()
        elif isinstance(module, nn.BatchNorm2d):
            module.weight.data.fill_(1)
            module.bias.data.zero_()

def get_lr_parameters(m: nn.Module, learning_rate: float, lr_group = Dict[str, float]):
    """
        Create different parameter groups with different learning rates
    """
    
    custom_lr_params = dict(
        (group_name, {"params": [], "lr": learning_rate*lr_factor[0], "weight_decay": lr_factor[1]}) 
        for group_name, lr_factor in lr_group.items()
    )
    custom_lr_params["default"] = {"params": [], "lr": learning_rate}

    for param_names, params in m.named_parameters():
        if not params.requires_grad:
            continue

        matches = False
        for group_name, lr in lr_group.items():
            if str.startswith(param_names,group_name):
                custom_lr_params[group_name]["params"].append(params)
                matches=True
                break
        
        if not matches:
            custom_lr_params["default"]["params"].append(params)

    return list(custom_lr_params.values())

def add_weight_decay(model: nn.Module, weight_decay: float = 1e-5, skip_list: Tuple = ()) -> List[Dict[str, Any]]:
    """Remove weight decay from BatchNorm and Bias parameters.

    https://discuss.pytorch.org/t/weight-decay-in-the-optimizers-is-a-bad-idea-especially-with-batchnorm/16994/3

    Args:
        model:
        weight_decay:
        skip_list:

    Returns:

    """
    decay = []
    no_decay = []
    for name, param in model.named_parameters():
        if not param.requires_grad:
            continue  # frozen weights
        if len(param.shape) == 1 or name.endswith(".bias") or name in skip_list:
            no_decay.append(param)
        else:
            decay.append(param)
    return [{"params": no_decay, "weight_decay": 0.0}, {"params": decay, "weight_decay": weight_decay}]


